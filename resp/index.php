    function showM(t) {
        let bar = $(".bar__module");
        if ($(t).hasClass("open")) {
            $(t).removeClass("open");
            bar.slideUp();
        } else {
            $(t).addClass("open");
            bar.slideDown();
        }
    }

    $(document).on('submit', 'form#file-form', function (e) {

        e.preventDefault();

        $('#loading-image').show();
        $('#loading').css('opacity', '0.3');
        $('.has-error').removeClass('has-error');

        var formData = new FormData(this);

        var url_file = "http://simbrella.azz/assets/file/Simbrella SimKredit™-ARPU-Increase-Case-Study.pdf";

        authWindow = window.open('about:blank', '', 'left=20,top=20,width=800,height=800,toolbar=0,resizable=1');

        $.ajax({
            url: 'ajax.php',
            type: 'POST',
            data: formData,
            contentType: false,
            cache: false,
            processData: false,
            dataType: 'json',
            success: function (data, textStatus, jqXHR) {
                if (data.code == 0) {
                    $('input[name="' + data.err_param + '"]').addClass('has-error');
                    authWindow.close();
                }
                else if (data.code == 11) {
                    $('div.checkbox_check').addClass('has-error_check');
                    authWindow.close();
                }
                else if (data.code == -1) {
                    $('div.error_letter').show();
                    authWindow.close();
                }
                else {
                    // $("p.lead").css("display","none");
                    // $("div.hidden_lead").css("display","none");
                    $("form#file-form").css("display", "none");
                    $('div.success_mail').show();
                    authWindow.location.replace(url_file);
                }

                $('#loading-image').hide();
                $('#loading').css('opacity', '1');
            },
            error: function (jqXHR, textStatus, errorThrown) {
                $('#loading-image').hide();
                $('#loading').css('opacity', '1');

            }
        });
    });
</script>

